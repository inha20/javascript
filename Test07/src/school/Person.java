package school;

public class Person {
    public String name;
    public double height;
    public double weight;
    public char gender;
    public Boolean married;

    public Person() {}
    public Person(String str) {
        this.name=name;
    }
    public Person(String str, double height, double weight) {
        this.name=name;
        this.height=height;
        this.weight=weight;
    }
}
